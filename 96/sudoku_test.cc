#include "sudoku.h"
#include "gtest/gtest.h"

TEST(sudoku, constructor) {
  sudoku s("123456789"
	   "456789123"
	   "789123456"
	   "234567891"
	   "567891234"
	   "891234567"
	   "345678912"
	   "678912345"
	   "912345678");
  EXPECT_TRUE(s.solve());
  EXPECT_EQ("123456789"
	       "456789123"
	       "789123456"
	       "234567891"
	       "567891234"
	       "891234567"
	       "345678912"
	       "678912345"
	       "912345678",s.get_solution());
}

